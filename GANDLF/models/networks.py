class LocalEnhancer(ModelBase_Blocks):
    def __init__(self, input_nc, output_nc, ngf=32, n_downsample_global=3, n_blocks_global=9, 
                 n_local_enhancers=1, n_blocks_local=3, norm_layer=nn.BatchNorm2d, padding_type='reflect', ndim=2, use_dropout=False):        
        ModelBase_Blocks.__init__(self, ndim, norm_layer)
        self.n_local_enhancers = n_local_enhancers
        
        if type(norm_layer) == functools.partial:  # no need to use bias as BatchNorm2d has affine parameters
            use_bias = norm_layer.func == self.InstanceNorm
        else:
            use_bias = norm_layer == self.InstanceNorm

        ###### global generator model #####           
        ngf_global = ngf * (2**n_local_enhancers)
        model_global = GlobalGenerator(input_nc, output_nc, ngf_global, n_downsample_global, n_blocks_global, norm_layer).model        
        model_global = [model_global[i] for i in range(len(model_global)-3)] # get rid of final convolution layers        
        self.model = nn.Sequential(*model_global)                

        ###### local enhancer layers #####
        for n in range(1, n_local_enhancers+1):
            ### downsample            
            ngf_global = ngf * (2**(n_local_enhancers-n))
            model_downsample = [self.ReflectionPad(3), self.Conv(input_nc, ngf_global, kernel_size=7, padding=0), 
                                norm_layer(ngf_global), nn.ReLU(True),
                                self.Conv(ngf_global, ngf_global * 2, kernel_size=3, stride=2, padding=1), 
                                norm_layer(ngf_global * 2), nn.ReLU(True)]
            ### residual blocks
            model_upsample = []
            for i in range(n_blocks_local):
                model_upsample += [ResnetBlock(ngf_global * 2, padding_type=padding_type, norm_layer=norm_layer,use_dropout=use_dropout, use_bias=use_bias, ndim=ndim)]

            ### upsample
            model_upsample += [self.ConvTranspose(ngf_global * 2, ngf_global, kernel_size=3, stride=2, padding=1, output_padding=1), 
                               norm_layer(ngf_global), nn.ReLU(True)]      

            ### final convolution
            if n == n_local_enhancers:                
                model_upsample += [self.ReflectionPad(3), self.Conv(ngf, output_nc, kernel_size=7, padding=0), nn.Tanh()]                       
            
            setattr(self, 'model'+str(n)+'_1', nn.Sequential(*model_downsample))
            setattr(self, 'model'+str(n)+'_2', nn.Sequential(*model_upsample))                  
        
        self.downsample = self.AvgPool(3, stride=2, padding=[1, 1], count_include_pad=False)

    def forward(self, input): 
        ### create input pyramid
        input_downsampled = [input]
        for i in range(self.n_local_enhancers):
            input_downsampled.append(self.downsample(input_downsampled[-1]))

        ### output at coarest level
        output_prev = self.model(input_downsampled[-1])        
        ### build up one layer at a time
        for n_local_enhancers in range(1, self.n_local_enhancers+1):
            model_downsample = getattr(self, 'model'+str(n_local_enhancers)+'_1')
            model_upsample = getattr(self, 'model'+str(n_local_enhancers)+'_2')            
            input_i = input_downsampled[self.n_local_enhancers-n_local_enhancers]            
            output_prev = model_upsample(model_downsample(input_i) + output_prev)
        return output_prev

class GlobalGenerator(ModelBase_Blocks):
    def __init__(self, input_nc, output_nc, ngf=64, n_downsampling=3, n_blocks=9, norm_layer=nn.BatchNorm2d, 
                 padding_type='reflect', ndim=2, use_dropout=False):
        
        assert(n_blocks >= 0)
        ModelBase_Blocks.__init__(self, ndim, norm_layer)
        self.activation = nn.ReLU(True) 
        
        if type(norm_layer) == functools.partial:  # no need to use bias as BatchNorm2d has affine parameters
            use_bias = norm_layer.func == self.InstanceNorm
        else:
            use_bias = norm_layer == self.InstanceNorm


        model = [self.ReflectionPad(3), self.Conv(input_nc, ngf, kernel_size=7, padding=0), norm_layer(ngf), self.activation]
        ### downsample
        for i in range(n_downsampling):
            mult = 2**i
            model += [self.Conv(ngf * mult, ngf * mult * 2, kernel_size=3, stride=2, padding=1),
                      norm_layer(ngf * mult * 2), self.activation]

        ### resnet blocks
        mult = 2**n_downsampling
        for i in range(n_blocks):
            model += [ResnetBlock(ngf * mult, padding_type=padding_type, norm_layer=norm_layer, use_dropout=use_dropout, use_bias=use_bias, ndim=ndim)]
        
        ### upsample         
        for i in range(n_downsampling):
            mult = 2**(n_downsampling - i)
            model += [self.ConvTranspose(ngf * mult, int(ngf * mult / 2), kernel_size=3, stride=2, padding=1, output_padding=1),
                       norm_layer(int(ngf * mult / 2)), self.activation]
        model += [self.ReflectionPad(3), self.Conv(ngf, output_nc, kernel_size=7, padding=0), nn.Tanh()]        
        self.model = nn.Sequential(*model)
            
    def forward(self, input):
        return self.model(input)   
    
class Encoder(ModelBase_Blocks):
    def __init__(self, input_nc, output_nc, ngf=32, n_downsampling=4, norm_layer=nn.BatchNorm2d, ndim=2, use_dropout=False):
        ModelBase_Blocks.__init__(self, ndim, norm_layer)
        self.output_nc = output_nc        

        model = [self.ReflectionPad(3), self.Conv(input_nc, ngf, kernel_size=7, padding=0), 
                 norm_layer(ngf), nn.ReLU(True)]             
        ### downsample
        for i in range(n_downsampling):
            mult = 2**i
            model += [self.Conv(ngf * mult, ngf * mult * 2, kernel_size=3, stride=2, padding=1),
                      norm_layer(ngf * mult * 2), nn.ReLU(True)]

        ### upsample         
        for i in range(n_downsampling):
            mult = 2**(n_downsampling - i)
            model += [self.ConvTranspose(ngf * mult, int(ngf * mult / 2), kernel_size=3, stride=2, padding=1, output_padding=1),
                       norm_layer(int(ngf * mult / 2)), nn.ReLU(True)]        

        model += [self.ReflectionPad(3), self.Conv(ngf, output_nc, kernel_size=7, padding=0), nn.Tanh()]
        self.model = nn.Sequential(*model) 

    def forward(self, input, inst):
        outputs = self.model(input)

        # instance-wise average pooling
        outputs_mean = outputs.clone()
        inst_list = np.unique(inst.cpu().numpy().astype(int))        
        for i in inst_list:
            for b in range(input.size()[0]):
                indices = (inst[b:b+1] == int(i)).nonzero() # n x 4            
                for j in range(self.output_nc):
                    output_ins = outputs[indices[:,0] + b, indices[:,1] + j, indices[:,2], indices[:,3]]                    
                    mean_feat = torch.mean(output_ins).expand_as(output_ins)                                        
                    outputs_mean[indices[:,0] + b, indices[:,1] + j, indices[:,2], indices[:,3]] = mean_feat                       
        return outputs_mean
    
class MultiscaleDiscriminator(ModelBase_Blocks):
    def __init__(self, input_nc, ndf=64, n_layers=3, norm_layer=nn.BatchNorm2d, 
                 use_sigmoid=False, num_D=3, getIntermFeat=False, ndim=2):
        ModelBase_Blocks.__init__(self, ndim, norm_layer)
        self.num_D = num_D
        self.n_layers = n_layers
        self.getIntermFeat = getIntermFeat
     
        for i in range(num_D):
            netD = NLayerDiscriminator(input_nc, ndf, n_layers, norm_layer, use_sigmoid, getIntermFeat)
            if getIntermFeat:                                
                for j in range(n_layers+2):
                    setattr(self, 'scale'+str(i)+'_layer'+str(j), getattr(netD, 'model'+str(j)))                                   
            else:
                setattr(self, 'layer'+str(i), netD.model)

        self.downsample = self.AvgPool(3, stride=2, padding=[1, 1], count_include_pad=False)

    def singleD_forward(self, model, input):
        if self.getIntermFeat:
            result = [input]
            for i in range(len(model)):
                result.append(model[i](result[-1]))
            return result[1:]
        else:
            return [model(input)]

    def forward(self, input):        
        num_D = self.num_D
        result = []
        input_downsampled = input
        for i in range(num_D):
            if self.getIntermFeat:
                model = [getattr(self, 'scale'+str(num_D-1-i)+'_layer'+str(j)) for j in range(self.n_layers+2)]
            else:
                model = getattr(self, 'layer'+str(num_D-1-i))
            result.append(self.singleD_forward(model, input_downsampled))
            if i != (num_D-1):
                input_downsampled = self.downsample(input_downsampled)
        return result
        
    

class DCGANDiscriminator(ModelBase_Blocks):
    def __init__(
        self,
        parameters, input_nc, ndf, norm_layer=nn.BatchNorm2d, ndim=2
    ):
        ModelBase_Blocks.__init__(self, ndim, norm_layer)


        def discriminator_block(in_filters, out_filters, bn=True):
            block = [self.Conv(in_filters, out_filters, 3, 2, 1), nn.LeakyReLU(0.2, inplace=True), self.Dropout(0.25)]
            if bn:
                block.append(norm_layer(out_filters, 0.8))
            return block
        if norm_layer!=None:
            bn=True
        else:
            bn=False
        self.model = nn.Sequential(
            *discriminator_block(input_nc, 16, bn=False),
            *discriminator_block(16, 32),
            *discriminator_block(32, 64),
            *discriminator_block(64, 128),
        )

        # The height and width of downsampled image
        ds_size = parameters["patch_size"][0] // 2 ** 4
        self.adv_layer = nn.Sequential(nn.Linear(128 * ds_size ** 2, 1), nn.Sigmoid())

    def forward(self, x):
        if len(x.shape) > 4:
            x = x.permute(0,1,4,2,3) #Sarthak
            x_part = x[:,0].clone()
        else:
            x_part = x.clone()
        x = self.model(x_part)
        x = x.view(x.shape[0], -1)
        out = self.adv_layer(x)

        return out


class DCGANGenerator(ModelBase_Blocks):
    def __init__(
        self,
        parameters, norm_layer, ndim=2
    ):
        ModelBase_Blocks.__init__(self, ndim, norm_layer)

        self.init_size = parameters["patch_size"][0] // 4
        self.l1 = nn.Sequential(nn.Linear(int(parameters["latent_dim"]), 128 * self.init_size ** 2))

        self.model = nn.Sequential(
            self.BatchNorm(128),
            nn.Upsample(scale_factor=2),
            self.Conv(128, 128, 3, stride=1, padding=1),
            self.BatchNorm(128, 0.8),
            nn.LeakyReLU(0.2, inplace=True),
            nn.Upsample(scale_factor=2),
            self.Conv(128, 64, 3, stride=1, padding=1),
            self.BatchNorm(64, 0.8),
            nn.LeakyReLU(0.2, inplace=True),
            self.Conv(64, parameters["model"]["num_channels"], 3, stride=1, padding=1),
            nn.Tanh(),
        )
        
    def forward(self, z):
        out = self.l1(z)
        out = out.view(out.shape[0], 128, self.init_size, self.init_size)
        out = self.model(out)
        
        return out